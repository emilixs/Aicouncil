export function extractKeywords(text: string): string[] {
  if (!text) return [];

  const words = text
    .toLowerCase()
    .replace(/[^\w\s]/g, '') // strip punctuation
    .split(/\s+/)
    .filter((w) => w.length >= 3 && !STOPWORDS.has(w));

  return [...new Set(words)];
}
